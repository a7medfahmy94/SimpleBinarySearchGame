    private void play(int x) {
        if (revealed(x)) {
            return;
        }
        increaseCounter();
        if (Integer.parseInt(requiredNumber.getText()) == numbers[x]) {
            revealNumbers();
        } else {
            revealNumber(x);
            if(requiredInt < numbers[x]){
                end = x;
            }else{
                start = x+1;
            }
            colorLabels();
        }
    }

    private void revealNumber(int x){
        labels[x].setText(Integer.toString(numbers[x]));
        labels[x].setForeground(Color.black);
    }
    
    private void revealNumbers(){
        for(int i = 1; i <= 99; i++){
            labels[i].setText(Integer.toString(numbers[i]));
        }
    }
    
    private void increaseCounter(){
        counter.setText(Integer.toString(Integer.parseInt(counter.getText())+1));
    }
    
    private boolean revealed(int x) {
        if (labels[x].getText() == "X" || labels[x].getText() == "HERE") {
            return false;
        }
        return true;
    }

    private void initPlay() {
        initLabels();
        generateNumbers();
        counter.setText("0");
        start = 0;
        end = 100;
    }

    private void generateNumbers() {
        int[] tmp = new int[100];
        for(int i = 1 ; i <= 100 ; i++){
            tmp[i-1] = (int)(Math.random()*(i*i*100));
        }
        numbers = new int[100];
        shuffleArray(tmp);
        for (int i = 1; i <= 99; i++) {
//            int random = (int) (Math.random() * 10000);
            numbers[i] = tmp[i-1];
        }
        int random = ((int) (Math.random() * 100)) % 99 + 1;
        setRequiredNumber(numbers[random]);
        Arrays.sort(numbers);
    }

      // Implementing FisherYates shuffle
    static void shuffleArray(int[] ar){
        Random rnd = new Random();
        for (int i = ar.length - 1; i > 0; i--){
          int index = rnd.nextInt(i + 1);
          // Simple swap
          int a = ar[index];
          ar[index] = ar[i];
          ar[i] = a;
        }
    }
    
    private void setRequiredNumber(int x) {
        requiredInt = x;
        requiredNumber.setText(Integer.toString(x));
    }

